
public class BasicEs004Main {

	public static void main(String[] args) {
		
		int result = -5 + (8 * 6);			// -5 + 8 * 6
		System.out.println(result);
		
		result = (55 + 9) % 9;
		System.out.println(result);
		
		result = 20 + (-3 * 5) / 8;			// 20 + -3 * 5 / 8
		System.out.println(result);
		
		result = 5 + (15 / 3) * 2 - (8 % 3);	// 5 + 15 / 3 * 2 - 8 % 3
		System.out.println(result);
	}

}
